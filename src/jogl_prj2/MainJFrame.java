    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        Integer selectedPotIndex = jComboBox1.getSelectedIndex();
        System.out.println(selectedPotIndex);
        switch (selectedPotIndex) {
            case 1: {
               RenderManagement.RenderManager.renderContext = RenderManagement.RenderManagementState.SQUARE_POT;
               theRenderingLogic.myRenderManagementUtil.cacheSquarePot();
               theRenderingLogic.myRenderManagementUtil.checkCurrentCachedData();
               break;
            }
            case 2: {
               RenderManagement.RenderManager.renderContext = RenderManagement.RenderManagementState.CLAY_POT;
               theRenderingLogic.myRenderManagementUtil.cacheClayPot();
               theRenderingLogic.myRenderManagementUtil.checkCurrentCachedData();
               break;
            }
            case 0:
            default: {
                RenderManagement.RenderManager.renderContext = RenderManagement.RenderManagementState.PYRMYD;
               break;
            }
                
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

