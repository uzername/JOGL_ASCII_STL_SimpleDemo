    private void MyOPENGL_activation() {
        renderGLCanvas = new GLCanvas();
        RenderManagement.RenderManager.renderContext= RenderManagement.RenderManagementState.PYRMYD;
        theRenderingLogic = new JavaRenderer2();
        renderGLCanvas.addGLEventListener(theRenderingLogic);
        jPanel1.add(renderGLCanvas, BorderLayout.CENTER);
        //rotation is performed only on each redraw. Animator runs display() in cycle:
        // https://www.tutorialspoint.com/jogl/jogl_rotation.htm
        animator = new FPSAnimator(renderGLCanvas, 300, true);
        animator.start();
    }
    
